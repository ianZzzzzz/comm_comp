def rfft_freq_filter(X:ndarray,bias: float)->ndarray:
    import numpy as np
    spectrum = np.fft.rfft(X)
    def filt(x):
        if x<bias:
            return 0
        else:
            return x
    filted = list(map(filt,spectrum))
    X = np.fft.irfft(filted)

    return X
def savgol_filter(series,window_length,k):
    ''''多项式拟合 参数：1 序列 2 窗口长度 3 多项式阶数'''
    import scipy.signal
    s_f= scipy.signal.savgol_filter(series,window_length,k)
    return s_f
